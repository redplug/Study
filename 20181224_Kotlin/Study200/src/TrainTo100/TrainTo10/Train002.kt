package TrainTo100.TrainTo10

fun main(args: Array<String>): Unit{
    53 + 62 - 126 // 계산은 하나 결과값이 화면상에 출력되지 않음
    println(53 + 62 - 126) // 계산하여 출력함
    println("53 + 62 - 126") // 문자값으로 인식하여 출력

    53 +
            62 - 126 // 줄을 변경하여도 정상적으로 인식함
    println(53
            + 62
            - 126) // 줄을 변경하여도 정상적으로 인식함(2)
}